# mpg 데이터 파악하기

# 1. ggplot2 패키지를 설치 (이미 설치했다면 이 과정은 생략)
install.packages("ggplot2")

# 2. ggplot2 패키지의 mpg 데이터를 불러와 데이터 프레임을 만들기
# as.data.frame()은 데이터 속성을 데이터 프레임 형태로 바꾸는 함수
mpg <- as.data.frame(ggplot2::mpg)

# 3. head(), tail() ... 를 이용해 데이터 살펴보기
head(mpg)    # raw 데이터 앞부분 확인
tail(mpg)    # raw 데이터 뒷부분 확인
dim(mpg)     # 행, 열 출력
str(mpg)     # 데이터 속성 확인(chr: 문자, num: 실수, int: 정수)
?mpg         # mpg 설명 글 출력
summary(mpg)    # 요약 통계량 출력